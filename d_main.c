// return gamemode for Doom or Ultimate Doom, use size to detect which one
gamemode_t GetDoomVersion (char* wadfile)
{
    struct stat sbuf;
    // and if I patch my main wad and the size gets
    // bigger ? uh?
    stat (wadfile, &sbuf);
    if (sbuf.st_size<12408292)
        return registered;
    else
        return retail;      // Ultimate
}

