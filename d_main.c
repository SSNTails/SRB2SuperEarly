//
// Find a Response File
//
void FindResponseFile (void)
{
    int             i;
#define MAXARGVS        256

    for (i = 1;i < myargc;i++)
        if (myargv[i][0] == '@')
        {
            FILE *          handle;
            int             size;
            int             k;
            int             index;
            int             indexinfile;
            char    *infile;
            char    *file;
            char    *moreargs[20];
            char    *firstargv;

            // READ THE RESPONSE FILE INTO MEMORY
            handle = fopen (&myargv[i][1],"rb");
            if (!handle)
            {
                CONS_Printf ("\nNo such response file!");
                exit(1);
            }
            CONS_Printf("Found response file %s!\n",&myargv[i][1]);
            fseek (handle,0,SEEK_END);
            size = ftell(handle);
            fseek (handle,0,SEEK_SET);
            file = malloc (size);
            fread (file,size,1,handle);
            fclose (handle);

            // KEEP ALL CMDLINE ARGS FOLLOWING @RESPONSEFILE ARG
            for (index = 0,k = i+1; k < myargc; k++)
                moreargs[index++] = myargv[k];

            firstargv = myargv[0];
            myargv = malloc(sizeof(char *)*MAXARGVS);
            memset(myargv,0,sizeof(char *)*MAXARGVS);
            myargv[0] = firstargv;

            infile = file;
            indexinfile = k = 0;
            indexinfile++;  // SKIP PAST ARGV[0] (KEEP IT)
            do
            {
                if ( *(infile+k) == '"' )       // strip encllosing double-quote
                    k++;
                myargv[indexinfile++] = infile + k;
                while ( (k < size) &&
                        ( (*(infile+k)> ' ') && (*(infile+k)<='z') ) )
                    k++;
                if ( *(infile+k-1) == '"' )     // strip ending double-quote
                    k--;
                *(infile+k) = 0;
                while(k < size &&
                      ((*(infile+k)<= ' ') || (*(infile+k)>'z')))
                    k++;
            } while(k < size);

            for (k = 0;k < index;k++)
                myargv[indexinfile++] = moreargs[k];
            myargc = indexinfile;

            // DISPLAY ARGS
            CONS_Printf("%d command-line args:\n",myargc);
            for (k=1;k<myargc;k++)
                CONS_Printf("%s\n",myargv[k]);

            break;
        }
}


